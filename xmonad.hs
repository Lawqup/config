-- Now run xmonad with all the hooks we set up.

main :: IO ()
main =  xmonad
        . ewmhFullscreen
        . ewmh
        . docks
        . xmobarProp
        . withEasySB (statusBarProp "xmobar" (pure myXmobarPP)) defToggleStrutsKey
        $ conf

conf = def {
      -- simple stuff
        terminal           = myTerminal,
        focusFollowsMouse  = myFocusFollowsMouse,
        clickJustFocuses   = myClickJustFocuses,
        borderWidth        = myBorderWidth,
        modMask            = myModMask,
        workspaces         = myWorkspaces,
        normalBorderColor  = myNormalBorderColor,
        focusedBorderColor = myFocusedBorderColor,

      -- key bindings
        keys               = myKeys,
        mouseBindings      = myMouseBindings,

      -- hooks, layouts
        layoutHook         = myLayout,
        manageHook         = myManageHook,
        handleEventHook    = myEventHook,
        logHook            = myLogHook,
        startupHook        = myStartupHook
    } `additionalKeys`
       [
        ((0, 0x1008FF11), spawn "amixer -q sset Master 4%-"),
        ((0, 0x1008FF13), spawn "amixer -q sset Master 4%+"),
        ((0, 0x1008FF12), spawn "amixer set Master toggle"),
        ((0, 0xFFC8), sendMessage $ Toggle FULL)
       ]
      `additionalKeysP`
      [
       ("M-f", spawn "firefox"),
       ("M-e", spawn "emacsclient -c -n"),
       ("M-r", spawn "xmonad --recompile; xmonad --restart"),
       ("M-S-<Space>", spawn "cyclekb us 'us(intl)'"),
       ("M-s", namedScratchpadAction myScratchPads "terminal"),
       ("M-q", namedScratchpadAction myScratchPads "calculator")
      ]

